def bubbleSort(alist):
    for passnum in range(len(alist)-1,0,-1):
        for i in range(passnum):
            if alist[i]>alist[i+1]:
                temp = alist[i]
                alist[i] = alist[i+1]
                alist[i+1] = temp
Sample_Data=[29,13,22,37,52,49,46,71,56]
bubbleSort(Sample_Data)
print(Sample_Data)


